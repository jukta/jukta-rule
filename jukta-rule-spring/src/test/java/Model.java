/**
 * @since 1.0
 */
public class Model {

    private String str;
    private int i;

    public Model(String str, int i) {
        this.str = str;
        this.i = i;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public int getI() {
        return i;
    }

    public void setI(int i) {
        this.i = i;
    }
}
